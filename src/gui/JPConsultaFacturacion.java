    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        LinkedList consulta = new LinkedList();


        try {

            consulta =cc.oficinasGanancias();

            Object[][] s = new Object[consulta.size()][3];
            for (int i = 0; i < consulta.size(); i++) {
                String[] retorno = (String[]) consulta.get(i);
                if (retorno[0] != null) {
                    s[i][0] = retorno[0];
                    s[i][1] = retorno[1];
                    s[i][2] = retorno[2];
                 

                } else {
                    s = null;
                }
            }
            TableModel myModel = new DefaultTableModel(s, new String[]{"Codigo Sucursal",
                "Nombre Sucursal","Activos"}) {

                boolean[] canEdit = new boolean[]{false, false,false};

                @Override
                public boolean isCellEditable(int rowIndex, int columnIndex) {
                    return canEdit[columnIndex];
                }
            };
            ///remover filas
            jTResultados3.setModel(myModel);
            jTResultados3.setRowSorter(new TableRowSorter(myModel));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

